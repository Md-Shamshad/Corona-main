package com.hp.psg.corona.common.beans;

import java.util.Date;

/**
 * @author dudeja
 * @version 1.0
 * 
 */
public class RegionalAttributeDefaults {

	private String radRegionCode;
	private String radObjectName;
	private String radAttributeName;
	private String radAttributeValue;
	private Date radCreatedDate;
	private String radCreatedBy;
	private Date radLastModifiedDate;
	private String radLastModifiedBy;

	public String getRadRegionCode() {
		return radRegionCode;
	}
	public void setRadRegionCode(String radRegionCode) {
		this.radRegionCode = radRegionCode;
	}
	public String getRadObjectName() {
		return radObjectName;
	}
	public void setRadObjectName(String radObjectName) {
		this.radObjectName = radObjectName;
	}
	public String getRadAttributeName() {
		return radAttributeName;
	}
	public void setRadAttributeName(String radAttributeName) {
		this.radAttributeName = radAttributeName;
	}
	public String getRadAttributeValue() {
		return radAttributeValue;
	}
	public void setRadAttributeValue(String radAttributeValue) {
		this.radAttributeValue = radAttributeValue;
	}
	public Date getRadCreatedDate() {
		return radCreatedDate;
	}
	public void setRadCreatedDate(Date radCreatedDate) {
		this.radCreatedDate = radCreatedDate;
	}
	public String getRadCreatedBy() {
		return radCreatedBy;
	}
	public void setRadCreatedBy(String radCreatedBy) {
		this.radCreatedBy = radCreatedBy;
	}
	public Date getRadLastModifiedDate() {
		return radLastModifiedDate;
	}
	public void setRadLastModifiedDate(Date radLastModifiedDate) {
		this.radLastModifiedDate = radLastModifiedDate;
	}
	public String getRadLastModifiedBy() {
		return radLastModifiedBy;
	}
	public void setRadLastModifiedBy(String radLastModifiedBy) {
		this.radLastModifiedBy = radLastModifiedBy;
	}

}
